          than a market order), that by its terms is effective until filled or
          cancelled by Client. GTC Orders are not automatically cancelled at the
          end of the Business Day on which they are placed.
          <br /><br />
          b) Limit – An order (other than a market order) to buy or sell the
          identified market at a specified price. A limit order to buy generally
          will be executed when the ask price equals or falls below the bid
          price that you specify in the limit order. A limit order to sell
          generally will be executed when the bid price equals or exceeds the
          ask price that you specify in the limit order.
          <br /><br />
          c) Market- An order to buy or sell the identified market at the
          current market price that Blue Sun Capital Ltd provides via the Online
          Trading System. An order to buy is executed at the current market ask
          price and an order to sell is executed at the current market bid
          price.
          <br /><br />
          d) One Cancels the Other (“OCO”) – An order that is linked to another
          order. If one of the orders is executed, the other will be
          automatically cancelled.
          <br /><br />
          e) Stop Loss – A stop loss order is an instruction to buy or sell a
          market at a price which is worse than the opening price of an open
          position (or worse than the prevailing price when applying the stop
          loss order to an already open position). It can be used to help
          protect against losses. Please note that because of market gapping,
          the best available price that may be achieved could be materially
          different to the price set on the stop loss order and as such, stop
          loss orders are not guaranteed to take effect at the price for which
          they are set.
          <br /><br />
          f) Trailing Stop – A trailing stop is the same as a stop loss order
          with the only difference being that, instead of setting a price at
          which the order is activated, the trailing stop order is activated at
          a fixed distance from the market price. For example, if Client has
          purchased a long open position and the market ask price increases, the
          trailing stop price will also increase and will trail behind the
          market ask price at the fixed distance set by Client. If the market
          ask price then decreases, the trailing stop price will remain fixed at
          its last position and if the market ask price reaches the trailing
          stop price, the order will be executed. Please note that because of
          market gapping, the best available price that may be achieved could be
          materially different to the price set on the trailing stop order and
          as such, trailing stop orders are not guaranteed to take effect at the
          fixed distance for which they are set. Following submission of an
          order, it is your sole responsibility to remain available for order
          and fill confirmations, and other communications regarding your
          Account until all open orders are completed. Thereafter, you must
